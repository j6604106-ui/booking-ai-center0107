def get_agent_prompt(agent_name: str) -> str:
    return AGENT_PROMPTS.get(agent_name, DEFAULT_PROMPT)


def assemble_user_message(user_text: str, knowledge_context: str) -> str:
    if not knowledge_context:
        return (
            f"{NO_KB_NOTE}\n\n"
            f"Вопрос пользователя: {user_text}"
        )
    return (
        f"Найденные статьи (цитируй их):\n\n"
        f"{knowledge_context}\n\n"
        f"Вопрос пользователя: {user_text}"
    )
